        from Armenia in major U.S. and European journals. </p>
    <p style="font-size: 12px;padding:8px;">To review a proposal, select it
        from the list that appears on the left. You can then click the <b>View/Print button</b> to
        see the content. At the top of the proposal summary page that you would see, you can click
        on a link to download the content of the <b>proposal narrative</b>. Once you have viewed the
        proposal to your satisfaction, you can do one of several things - all functions accessible
        through buttons near the bottom of your portal: <br>(1) <b>Reject to review</b> the proposal, in
        case you feel you cannot review the proposal in question; <br>(2) <b>Save and submit review,</b>
        after you fill out scores for ALL seven rating criteria; <br>(3) <b>Save but do not submit</b>,
        which allows you to save some of your scores but leave the rest for another time to
        complete; <br>(4) <b>Send a message</b> to the administrator assigned to the proposal, for
        example for questions or further information about the proposal at hand.</p>
    <p style="font-size: 12px;padding:8px;">Each proposal needs a score in
        one of <b>seven</b> different criteria described below. Each score can be one of six grades:
        Outstanding, Excellent, Very Good, Good, Fair, Poor. You can also write comments about the
        proposal: these are seen by our Research Board during the final evaluation stage and are
        <strong>not</strong> communicated to the PIs.</p>
    <p style=" color:#a00; font-size: 12px;padding:8px;"><b>All proposals
            are to be deemed confidential.</b></p>
